#сортировка методом Шелла 

def shell_sort(arr):
    n = len(arr)  # Определяем длину массива
    gap = n // 2  # Устанавливаем начальный размер шага

    # Основной цикл метода Шелла
    while gap > 0:
        # Проход по массиву с шагом gap
        for i in range(gap, n):
            temp = arr[i]  # Временное хранение текущего элемента
            j = i
            # Сравнивание и сдвиг элементов на расстояние gap
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]  # Перенос элемента на позицию дальше
                j -= gap  # Смещение на шаг назад
            arr[j] = temp  # Размещение временного элемента на правильную позицию
        gap //= 2  # Уменьшение размера шага вдвое

# Выводим приглашение для ввода
print("Введите последовательность чисел:")

# Читаем ввод, разбиваем его на список строк и преобразуем в список целых чисел
numbers = list(map(int, input().strip().split()))

# Сортируем список методом Шелла
shell_sort(numbers)

# Выводим отсортированный список
print(' '.join(map(str, numbers)))
