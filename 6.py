#сортировка посредством выбора

def selection_sort(arr):
    n = len(arr)  # Определяем длину массива
    for i in range(n):  # Внешний цикл для прохода по всем элементам массива
        min_idx = i  # Предполагаем, что первый элемент является минимальным
        for j in range(i + 1, n):  # Внутренний цикл для поиска минимального элемента
            if arr[min_idx] > arr[j]:  # Если находим элемент меньше текущего минимума
                min_idx = j  # Обновляем индекс минимального элемента
        arr[i], arr[min_idx] = arr[min_idx], arr[i]  # Меняем местами текущий элемент и минимальный

# Выводим приглашение для ввода
print("Введите последовательность чисел:")

# Читаем ввод, разбиваем его на список строк и преобразуем в список целых чисел
numbers = list(map(int, input().strip().split()))

# Сортируем список методом выбора
selection_sort(numbers)

# Выводим отсортированный список
print(' '.join(map(str, numbers)))
