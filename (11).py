# быстрая сортировка

def quick_sort(arr):
    # Базовый случай: если длина массива меньше или равна 1,
    # то он уже отсортирован, возвращаем его без изменений.
    if len(arr) <= 1:
        return arr

    # Выбираем центральный элемент массива в качестве опорного.
    pivot = arr[len(arr) // 2]

    # Разделяем массив на три части:
    # элементы меньше опорного, равные ему и больше него.
    left = [x for x in arr if x < pivot]      # Элементы меньше опорного
    middle = [x for x in arr if x == pivot]   # Элементы равные опорному
    right = [x for x in arr if x > pivot]     # Элементы больше опорного

    # Рекурсивная сортировка левой и правой частей, объединение результатов.
    return quick_sort(left) + middle + quick_sort(right)

# Запрашиваем у пользователя ввод последовательности чисел.
print("Введите последовательность чисел:")

# Преобразуем введенную строку в список целых чисел.
numbers = list(map(int, input().strip().split()))

# Сортируем введенный список чисел.
sorted_numbers = quick_sort(numbers)

# Печатаем отсортированный список, преобразуя числа обратно в строки.
print(' '.join(map(str, sorted_numbers)))
