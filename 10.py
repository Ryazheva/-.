# сортировка методом слияния

def merge_sort(arr):
    """
    Реализует сортировку методом слияния (Merge Sort).

    :param arr: Исходный массив, который нужно отсортировать.
    :return: Отсортированный массив.
    """

    # Базовый случай: если длина массива меньше или равна 1, он уже отсортирован.
    if len(arr) <= 1:
        return arr

    # Находим середину массива для разделения его на две части.
    mid = len(arr) // 2

    # Рекурсивно вызываем merge_sort для левой и правой половин массива.
    left_half = merge_sort(arr[:mid])  # Левая половина
    right_half = merge_sort(arr[mid:])  # Правая половина

    # Возвращаем результат объединения двух отсортированных половин.
    return merge(left_half, right_half)


def merge(left, right):
    """
    Объединяет две отсортированные половины массива в одну отсортированную последовательность.

    :param left: Первая отсортированная часть массива.
    :param right: Вторая отсортированная часть массива.
    :return: Полностью отсортированный массив.
    """

    # Создаем пустой список для хранения результата.
    result = []
    i = j = 0

    # Сравниваем элементы обеих половин и добавляем наименьший в результат.
    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    # Добавляем оставшиеся элементы из левой половины, если таковые есть.
    while i < len(left):
        result.append(left[i])
        i += 1

    # Добавляем оставшиеся элементы из правой половины, если таковые есть.
    while j < len(right):
        result.append(right[j])
        j += 1

    # Возвращаем полностью отсортированный массив.
    return result


# Основной блок программы
print("Введите последовательность чисел:")

# Получаем от пользователя строку чисел и преобразуем её в список целых чисел.
numbers = list(map(int, input().strip().split()))

# Вызываем функцию merge_sort для сортировки введенного списка.
sorted_numbers = merge_sort(numbers)

# Выводим отсортированный список, преобразовав его в строку через пробел.
print(' '.join(map(str, sorted_numbers)))
